// Let's begin

// What is programming?
// Programming is the process of designing and writing instructions
// that a computer can execute to solve a problem.

// Humans give instructions to a computer using programming languages,
// and the computer executes those instructions step by step.

// Programming converts human ideas or algorithms into
// machine-executable instructions.

// There are many programming languages such as:
// C++, Python, Java, C#, C, and Assembly Language.

// Some programming languages are called "low-level" languages.
// Example: Assembly Language
// They are very close to machine language,
// difficult for humans to read and write,
// but offer maximum control over hardware and performance.

// Some programming languages are called "high-level" languages.
// Example: Python
// They are easier for humans to read and write,
// but provide less direct control over hardware.

// C++ is often considered a middle-level language.
// It combines high-level features (classes, functions, libraries)
// with low-level features (memory management, pointers).

// C++ is a compiled language.
// This means the source code is translated into machine code
// before the program is executed.

// Where do we write our programs?
// We write programs using an IDE (Integrated Development Environment)
// such as Code::Blocks, Visual Studio, or VS Code.
// An IDE helps us write, compile, run, and debug our code.

// In C++, comments are used to explain code and improve readability.
// The compiler ignores comments during execution.

